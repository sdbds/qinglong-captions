class I18n:
    """Internationalization class"""

    def __init__(self, lang: str = "zh"):
        self.lang = lang if lang in TRANSLATIONS else "zh"

    def t(self, key: str, default: str = None):
        """Get translation for key"""
        keys = key.split(".")
        value = TRANSLATIONS.get(self.lang, {})

        for k in keys:
            if isinstance(value, dict) and k in value:
                value = value[k]
            else:
                return default if default is not None else key

        return value if value is not None else (default if default is not None else key)

    def set_language(self, lang: str):
        """Set current language"""
        if lang in TRANSLATIONS:
            self.lang = lang


# Global i18n instance
_i18n = I18n()


def t(key: str, default: str = None) -> str:
    """Get translation for key"""
    return _i18n.t(key, default)


def set_language(lang: str):
    """Set current language"""
    _i18n.set_language(lang)


def get_i18n() -> I18n:
    """Get i18n instance"""
    return _i18n
